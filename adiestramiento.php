<h2>Adiestramiento</h2>

<article class="adiestramiento">
    <img src="img/adiestramiento-foto.png" alt="No image" title="Adiestramiento">
    <div>
        <p>En nuestra clínica veterinaria, nuestro equipo de expertos en comportamiento animal utiliza técnicas positivas y personalizadas para cada mascota, asegurando un proceso de aprendizaje efectivo y respetuoso.</p>
        <ul>
            <li><b>Obediencia Básica:</b> Enseñanza de comandos esenciales como sentarse, quedarse, venir y caminar con correa.</li>
            <li><b>Adiestramiento Avanzado:</b> Entrenamiento de comandos más complejos y habilidades específicas según las necesidades del propietario.</li>
            <li><b>Modificación de Conducta:</b> Intervenciones para corregir comportamientos indeseados como ladridos excesivos, agresividad o ansiedad por separación.</li>
            <li><b>Socialización de Cachorros:</b> Sesiones diseñadas para exponer a los cachorros a diferentes personas, animales y entornos.</li>
            <li><b>Entrenamiento para la Respuesta a Emergencias:</b> Enseñanza de comandos y comportamientos que pueden ser cruciales en situaciones de emergencia.</li>
            <li><b>Entrenamiento para Deportes y Agilidad:</b> Preparación para competencias y actividades deportivas como agility, flyball y obediencia competitiva.</li>
            <li><b>Entrenamiento para Terapia y Asistencia:</b> Formación para mascotas que se convertirán en animales de terapia o asistencia.</li>
        </ul>
    </div>
</article>