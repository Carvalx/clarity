<h2>Diagnóstico en nuestra Veterinaria</h2>
<article class="diagnostico">
    <img src="img/operacion.jpg" alt="No image" title="Diagnóstico">
    <div>
        <p>En nuestra clínica veterinaria, ofrecemos una amplia gama de opciones de diagnóstico para asegurar la salud y el bienestar de tus mascotas. Algunos de los servicios diagnósticos que proporcionamos incluyen:</p>
        <ul>
            <li><b>Exámenes Físicos Completos:</b> Evaluaciones detalladas para detectar cualquier problema de salud en tus mascotas.</li>
            <li><b>Análisis de Sangre y Orina:</b> Para identificar infecciones, enfermedades y condiciones metabólicas.</li>
            <li><b>Rayos X y Ecografías:</b> Imágenes detalladas para evaluar huesos, órganos y tejidos internos.</li>
            <li><b>Endoscopias:</b> Procedimientos mínimamente invasivos para examinar el interior del cuerpo.</li>
            <li><b>Pruebas de Alergia:</b> Identificación de alergias alimentarias o ambientales.</li>
            <li><b>Electrocardiogramas (ECG):</b> Monitoreo de la salud cardiovascular.</li>
            <li><b>Biopsias:</b> Toma de muestras de tejidos para un análisis más profundo.</li>
        </ul>
    </div>
</article>