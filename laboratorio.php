<h2>Laboratorio en nuestra Veterinaria</h2>
<article class="laboratorio">
    <img src="img/laboratorio-foto.jpg" alt="No image" title="Laboratorio">
    <div>
        <p>Nuestros servicios de laboratorio veterinario están diseñados para proporcionar diagnósticos rápidos y precisos, asegurando que tu mascota reciba el tratamiento adecuado en el menor tiempo posible. Confía en nosotros para cuidar la salud de tus perros, gatos, roedores y aves con la mejor tecnología y dedicación profesional:</p>
        <ul>
            <li><b>Hemogramas Completos:</b> Análisis detallado de los componentes sanguíneos (glóbulos rojos, glóbulos blancos, plaquetas).</li>
            <li><b>Perfiles Bioquímicos:</b> Evaluación de enzimas y metabolitos en sangre.</li>
            <li><b>Análisis de Orina:</b> Examen de la composición y propiedades de la orina.</li>
            <li><b>Análisis de Heces:</b> Evaluación de parásitos intestinales y microflora.</li>
            <li><b>Pruebas Serológicas:</b> Detección de anticuerpos y antígenos en sangre.</li>
            <li><b>Pruebas de Función Hepática y Renal:</b> Medición de enzimas específicas y metabolitos.</li>
            <li><b>Citologías:</b> Análisis de células de diversas muestras (piel, masas, líquidos).</li>
            <li><b>Cultivos Bacterianos y Fúngicos:</b> Identificación de bacterias y hongos presentes en muestras.</li>
        </ul>
    </div>
