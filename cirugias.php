<h2>Cirugías en nuestra Veterinaria</h2>
<article class="cirugias">
    <img src="img/cirugia-foto.jpg" alt="No image" title="Cirugías">
    <div>
        <p>En nuestra clínica veterinaria, ofrecemos una amplia gama de servicios quirúrgicos para asegurar el bienestar de tus mascotas. Algunas de las cirugías que proporcionamos incluyen:</p>
        <ul>
            <li><b>Cirugía de Esterilización (Castración y Ovariohisterectomía):</b> Procedimientos para controlar la reproducción y mejorar la salud de tus mascotas.</li>
            <li><b>Cirugía de Tejidos Blandos:</b> Tratamientos para problemas como tumores, hernias, y obstrucciones gastrointestinales.</li>
            <li><b>Cirugía Ortopédica:</b> Reparación de fracturas, dislocaciones y problemas articulares.</li>
            <li><b>Cirugía Dental:</b> Extracciones dentales y tratamientos de enfermedades periodontales.</li>
            <li><b>Cirugía de Emergencia:</b> Atención inmediata para lesiones graves y condiciones críticas.</li>
            <li><b>Cirugía Ocular:</b> Tratamientos para cataratas, úlceras corneales y otras condiciones oculares.</li>
            <li><b>Cirugía Reconstructiva:</b> Reparación de lesiones y corrección de malformaciones.</li>
        </ul>
    </div>
</article>