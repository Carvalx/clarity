<h2>Laserterapia en nuestra Veterinaria</h2>
<article class="laserterapia">
    <img src="img/laserterapia-foto.jpeg" alt="No image" title="Laserterapia">
    <div>
        <p>Ofrecemos servicios de laserterapia para proporcionar alivio y acelerar la recuperación de tus mascotas. La laserterapia es una técnica no invasiva que utiliza la luz láser para estimular la regeneración de tejidos, reducir la inflamación y aliviar el dolor.</p>
        <ul>
            <li><b>Tratamiento del Dolor Crónico:</b> Utilización del láser para reducir el dolor asociado con condiciones crónicas como la artritis.</li>
            <li><b>Recuperación Postquirúrgica:</b> Aplicación de laserterapia para acelerar la cicatrización de heridas postoperatorias.</li>
            <li><b>Lesiones Musculoesqueléticas:</b> Tratamiento de esguinces, distensiones y otras lesiones musculares y articulares.</li>
            <li><b>Problemas Dermatológicos:</b> Tratamiento de afecciones de la piel como dermatitis, heridas y quemaduras.</li>
            <li><b>Enfermedades Inflamatorias:</b> Aplicación de láser para reducir la inflamación en condiciones como la otitis y la gingivitis.</li>
            <li><b>Terapia de Rehabilitación:</b> Uso de laserterapia como parte de programas de rehabilitación para mascotas con movilidad reducida.</li>
        </ul>
    </div>
</article>