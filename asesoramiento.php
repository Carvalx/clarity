<h2>Asesoramiento en nuestra Veterinaria</h2>
<article class="asesoramiento">
    <img src="img/asesoramiento-foto.jpg" alt="No image" title="Asesoramiento">
    <div>
        <p>En nuestra clínica veterinaria, ofrecemos servicios de asesoramiento especializados para ayudarte a cuidar de tus mascotas de la mejor manera posible. Nuestro equipo de expertos está aquí para proporcionarte la orientación y el apoyo que necesitas en diversas áreas de la salud y el bienestar animal:</p>
        <ul>
            <li><b>Nutrición y Dieta:</b> Orientación sobre la alimentación adecuada para cada etapa de la vida de tu mascota.</li>
            <li><b>Prevención de Enfermedades:</b> Consejos sobre vacunaciones, desparasitaciones y medidas preventivas.</li>
            <li><b>Cuidados Geriátricos:</b> Asesoramiento para el cuidado de mascotas mayores.</li>
            <li><b>Entrenamiento y Comportamiento:</b> EConsejos y técnicas para el entrenamiento y manejo de problemas de comportamiento.</li>
            <li><b>Reproducción y Cría:</b> Orientación sobre la reproducción responsable y el cuidado de camadas.</li>
            <li><b>Cuidado Postoperatorio:</b> Instrucciones y apoyo para el cuidado de tu mascota después de una cirugía.</li>
            <li><b>Higiene y Cuidado General:</b> Consejos sobre el aseo, cuidado dental y mantenimiento general.</li>
        </ul>
    </div>
